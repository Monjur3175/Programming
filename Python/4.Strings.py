#String is Immutable

name = "Monjur"
print(name)

print(name[0:3])#starts from 0 to all the way to 2(3-1)
print(name[1:4])#starts from 1 to all the way to 3(4-1)
print(name.upper())
print(name.capitalize())
print(name.count("o"))
print(name.isalnum())
print(name.isnumeric())
print(name.isprintable())
